package net.ostis.scpdev.editors;

import org.eclipse.core.resources.IStorage;
import org.eclipse.core.runtime.CoreException;
import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.ui.IPersistableElement;
import org.eclipse.ui.IStorageEditorInput;

/**
 * Just hold and return {@link IStorage IStorage} instance.
 *
 * @author Dmitry Lazurkin
 */
public class StorageEditorInput implements IStorageEditorInput {

    private final IStorage storage;

    /**
     * @param storage Hold and return this storage
     */
    public StorageEditorInput(IStorage storage) {
        this.storage = storage;
    }

    @Override
    public IStorage getStorage() throws CoreException {
        return storage;
    }

    @Override
    public boolean exists() {
        return false;
    }

    @Override
    public ImageDescriptor getImageDescriptor() {
        return null;
    }

    @Override
    public String getName() {
        return null;
    }

    @Override
    public IPersistableElement getPersistable() {
        return null;
    }

    @Override
    public String getToolTipText() {
        return null;
    }

    @SuppressWarnings("rawtypes")
    @Override
    public Object getAdapter(Class adapter) {
        return null;
    }
}
